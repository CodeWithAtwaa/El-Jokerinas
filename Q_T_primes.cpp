int n = 1000000;
vector<bool> isPrime(n + 1, true);
void sieve()
{
    isPrime[0] = isPrime[1] = false;
    for (int i = 2; i * i <= n; i++)
    {
        if (isPrime[i])
        {
            for (int j = i * i; j <= n; j += i)
            {
                isPrime[j] = false;
            }
        }
    }
}
inline void Sokan_El_Leil()
{
    sieve();
    cin >> n;

    while(n -- ) {
        int x;
        cin >> x;
        int s = sqrt(x);
        if(s * s == x && isPrime[s])
            cout << "YES\n";
        else
            cout << "NO\n";

    }
    
}

